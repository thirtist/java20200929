package chap06.textbook.exercises.p17;

public class Printer {
	static void println(int para) {
		System.out.println(para);
	}
	static void println(boolean para) {
		System.out.println(para);
	}
	static void println(double para) {
		System.out.println(para);
	}
	static void println(String para) {
		System.out.println(para);
	}
}
