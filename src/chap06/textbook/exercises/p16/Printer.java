package chap06.textbook.exercises.p16;

public class Printer {
	void println(int para) {
		System.out.println(para);
	}
	void println(boolean para) {
		System.out.println(para);
	}
	void println(double para) {
		System.out.println(para);
	}
	void println(String para) {
		System.out.println(para);
	}
}
